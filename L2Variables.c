#include <stdio.h>
int main()
{
    int a = 4;
    // int b = 8.5 This is wrong because 8.5 is not a Integer
    float b = 8.5;
    char c = 'u';
    int d = 69;

    printf("The value of a is %d \n", a);
    printf("The value of b is %f \n", b);
    printf("The value of c is %c \n", c);
    printf("The sum of a&d is %d \n \n \n", a + d);

    return 0;
}
